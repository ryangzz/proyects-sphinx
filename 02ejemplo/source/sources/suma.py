"""
Definicion de funcion suma
==========================
Esta **Funcion Suma** se encargara de sumar sus parametros
y retornar el resultado de la operacion
"""
def suma(a,b):
    """"
    se reciben los parametros y se retorna directamente
    la suma en el retorno
    """
    return a+b
"""
Se llama a la funcion  e imprimimos el resultado
esto debe arrojar 5
"""
print(suma(2,3))